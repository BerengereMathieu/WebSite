    <h2 >Activité de recherche</h2>
    <div class="row">
      <div class="col-sm-12">
      <h4 class="cv_subtitle">Sujet de thèse : Segmentation sémantique d'images pour l'analyse de l'évolution d'une scène </h4>
      <div class="cv_description">
      <p>
	La segmentation sémantique consiste à identifier et à localiser les objets qui
	composent une image. S'il s'agit d'une tâche que nous réalisons en général
	facilement, l'automatiser afin qu'elle soit effectuée par un ordinateur, s'avère
	un problème complexe. Les travaux menés durant les trois dernières décennies
	par les chercheurs académiques, notamment sous l'impulsion de grandes entreprises
	comme Google et Microsoft, ont permis des progrès significatifs dans cette thématique.
      </p>
      <p>
	Néanmoins, les méthodes proposées requièrent une étape d'apprentissage, impliquant
	de nombreuses photographies pourvues de segmentations de référence, c'est-à-dire
	d'images où un être humain est venu attribuer chaque pixel à une classe sémantique
	(ciel, herbe, avion etc.). L'obtention de ces images est une opération qui se
	révèle fastidieuse et coûteuse. Dans le cadre de nombreuses applications,
	cette difficulté devient rédhibitoire.

      </p>
      <p>
	Le travail effectué durant ma thèse consiste à explorer des méthodes de segmentation
	sémantique capables de relâcher cette contrainte, notamment en faisant appel à
	une interaction avec l'utilisateur, pour guider le programme. Cette recherche
	s'inscrit dans le cadre d'un projet en partenariat avec le laboratoire de
	géographie GEODE (Géographie de l'Environnement) ainsi qu'avec la DREAL
	(Direction Régionale de l'Environnement, de l'Aménagement et du Logement),
	visant à produire une solution logicielle capable de signaler automatiquement
	des changements significatifs dans une série de photographies représentant
	un même lieu à des époques différentes.

      </p>
      </div>
      </div>
      </div>

