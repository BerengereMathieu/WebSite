      <h4 class="cv_subtitle">Sur-segmentation en superpixels</h4>
      <div class="cv_description">
	<p><strong>Contexte : </strong> Stage en troisième année de licence</p>
	<p><strong>Problématique : </strong>La sur-segmentation en superpixels consiste
	  à créer une partition de l'image en petits ensembles de pixels connexes
	  et similaires : les superpixels. Ces superpixels, pourront ensuite être
	  utilisés à la place des pixels. Moins nombreux, ils permettent souvent
	  d'accélérer les traitements ultérieurs.</p>
	<p><strong>Travail : </strong>Étude d'un algorithme de sur-segmentation en
	  superpixels, SLIC, et modification de cette méthode pour qu'elle utilise une
	  information de texture en plus de la couleur et de la position des pixels.</p>
